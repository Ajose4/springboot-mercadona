package mercadona.springbootapp.dto;

public class DestinyDTO {

	/** cod */
	private Integer cod;
	
	/** name */
	private String name;
	
	public DestinyDTO() {
		super();
	}
	
	public Integer getCod() {
		return cod;
	}
	public String getName() {
		return name;
	}

	public void setCod(Integer cod) {
		this.cod = cod;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
