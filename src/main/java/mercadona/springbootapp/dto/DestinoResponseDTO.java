package mercadona.springbootapp.dto;

public class DestinoResponseDTO {

	/** cod */
	private String cod;
	
	/** name */
	private String name;
	
	public DestinoResponseDTO() {
		super();
	}
	
	public String getCod() {
		return cod;
	}
	public String getName() {
		return name;
	}

	public void setCod(String cod) {
		this.cod = cod;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
